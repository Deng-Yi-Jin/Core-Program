#include "libft.h"

void ft_putendl_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if(!s)
		return ;
	while (s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
	s[i] = '\0';
	write(fd , "\0", 1);
}
